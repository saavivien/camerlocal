package com.camerlocal.camerlocal.exception;

/**
 *
 * @author vivien saa
 */
public class CamerLocalDaoException extends CamerLocalException {

    public CamerLocalDaoException() {
        super();
    }

    public CamerLocalDaoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public CamerLocalDaoException(String message, Throwable cause) {
        super(message, cause);
    }

    public CamerLocalDaoException(String message) {
        super(message);
    }

    public CamerLocalDaoException(Throwable cause) {
        super(cause);
    }
}
